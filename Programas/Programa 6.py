# Tipos de datos en python

# 1) largo[long] y entero [int]

numero = 15
print(numero, type(numero))
"manera diferente de  concatenar codigos, en esta linea se puede ver el uso del type para saber que tipo de dato contiene mi variable."

# 2) numero flotante [float]

numero_flotante = 13.8
print(numero_flotante, type(numero_flotante))

# 3) numero complejo

numero_complejo = 13 + 8j
print(numero_complejo, type(numero_complejo))

# 4) Strings

hol = "lolitajer"
print(hol, type(hol))

# 5) tipo de dato bol

true_false = 3 == 4
print(true_false, type(true_false))