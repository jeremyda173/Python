#Operadores aritmeticos

#Adicion o suma

numero_1 = 90
numero_2 = 11
numero_3 = 2
numero_5 = 10

resultado = numero_1 + numero_2
print("El resultado de la suma sera: " + str(resultado))

#Sustraccion o resta

resultado = numero_1 - numero_2
print("El resultado de la resta es: " + str(resultado))

#Multiplicacion

resultado = numero_1 * numero_2
print("El resultado de la multiplicacion sera: " + str(resultado))

#Potencia

resultado = numero_3**3
print("El resultado de la potencia sera: " + str(resultado))

#Division

resultado = numero_1 / numero_3
print("El resultado de la idivision  es: " + str(resultado))

#Modulo o resto

resultado = numero_5 % numero_3
print("El modulo o resto es: " + str(resultado))

#Division entera

resultado = numero_1 // numero_3
print("El resultado de la idivision  es: " + str(resultado))
