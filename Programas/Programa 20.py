# Metodo .strip() 

Nombre = "Jeremy"

Nombre = Nombre.strip("s jMy") # Comprueba caracter por caracter de la cadena de texto
print(Nombre)

# Metodo .rstrip()

nombre = " a Jeremy "

nombre = nombre.rstrip("s jMy") # Comprueba caracter por caracter de la cadena de texto
print(nombre) # elimina caracteres al final de la cadena

